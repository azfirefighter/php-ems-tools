<h3>PHP EMS Tools Positions Roster Help</h3>
<p>For the administrative view of the roster, please go to the PHP EMS Tools
index page and select Positions Roster Administrative View, or replace the current URL
(address) of "roster.php" with "roster.php?adminView=1".</p>
<p>To sort the roster by any of those fields, click on the column title (link)
and the roster will be re-sorted by that column.</p>

<p>
The positions roster shows all members' settings for Officer positions, other positions, committees, and committee position. Each member may have an officer position, an administrator position, and two committees with positions set. The possible values for the these are defined in custom.php.
</p>
<p>
To set these values, see the Administrative View of the Positions Roster. This page allows the editing of values for all members. You must enter your ID and password to edit the values, and must have a rights level of 1 or greater.

<p>For full documentatin, please see <a href="index.html">the docs</a> or the
<a href="http://www.php-ems-tools.com">PHP EMS Tools Homepage</a></p>
